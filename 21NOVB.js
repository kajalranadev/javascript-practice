// Initialize the firstArray values [1, 2, 3, 4]
let firstArray = [1, 2, 3, 4];

// Assign the reference of firstArray to secondArray
let secondArray = firstArray;

// Modify the secondArray by adding 5 to the end
secondArray.push(5);

// firstArray to the console
console.log(firstArray); // [1, 2, 3, 4, 5]

// secondArray to the console
console.log(secondArray); // [1, 2, 3, 4, 5]
